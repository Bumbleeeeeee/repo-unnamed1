package GraphicsEngine;

public class Normal {
    
    float valA;
    float valB;
    float valC;

    public Normal(float a, float b, float c){valA = a; valB = b; valC = c;}

    public float getValA() {
        return valA;
    }

    public float getValB() {
        return valB;
    }

    public float getValC() {
        return valC;
    }
}
